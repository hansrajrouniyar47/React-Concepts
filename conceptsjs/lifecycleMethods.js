// React has 4 different lifecycle methods basically,

// 1. Mounting : When an instance of a component is being created and inserted into the DOM 

// 2. Updating : When a component is being re-rendered as a result of changes to either its props or state 

// 3. Unmounting : When a component is being removed from the DOM

// 4. Error Handling : When there is an error during rendering in a lifecycle  or in the constructor of any child component

// Now let's discuss about different methods involved in each phase

// 1. Mounting : constructor, static getDerivedStateFromProps, render and componentDidMount

// 2. Updating : static getDerivedStateFromProps, shouldComponentUpdate, render, getSnapshotBeforeUpdate and componentDidUpdate

// 3. Unmounting : componentWillUnmount

// 4. Error Handling : static getDerivedStateFromError and componentDidCatch



